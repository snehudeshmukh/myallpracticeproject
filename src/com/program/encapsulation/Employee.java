package com.program.encapsulation;

public class Employee {
	private String name;
	private int employeeId;
	
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getEmployeeId() {
	return employeeId;
}
public void setEmployeeId(int id) {
	this.employeeId = id;
}
}